import { useEffect } from "react"

import { loginWithGitHub, loginwithGoogle } from "../firebase/client"
import useUser, { USER_STATES } from "hooks/useUser"

import Head from "next/head"
import Router from "next/router"

import AppLayout from "components/AppLayout"
import Button from "components/Button"
import GitHubIcon from "components/Icons/GitHub"
import GoogleIcon from "components/Icons/Google"
import Spinner from "components/Spinner"

import { colors } from "styles/theme"

export default function Home() {
  const user = useUser()

  useEffect(() => {
    user && Router.replace("/home")
  }, [user])

  const handleClickLoginGitHub = () => {
    loginWithGitHub().catch((err) => {
      console.log(err)
    })
  }

  const handleClickLoginGoogle = () => {
    loginwithGoogle().catch((err) => {
      console.log(err)
    })
  }

  return (
    <>
      <Head>
        <title>Nexter </title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <AppLayout>
        <section>
          <img src="/cubo-logo.png" alt="logo" />
          <h1>Nexter</h1>
          <h2>
            Talk about development <br /> with developers
          </h2>
          <div>
            {user === USER_STATES.NOT_LOGGED && (
              <section className="login-buttons-cotainer">
                <Button onClick={handleClickLoginGitHub}>
                  <GitHubIcon fill="#fff" width={32} height={32} />
                  Login with GitHub
                </Button>
                <Button onClick={handleClickLoginGoogle}>
                  <GoogleIcon width={32} />
                  Login with Google
                </Button>
              </section>
            )}
            {user === USER_STATES.UNKNOW && <Spinner />}
          </div>
        </section>
      </AppLayout>

      <style jsx>{`
        section {
          height: 100%;
          display: grid;
          place-content: center;
          place-items: center;
        }

        .login-buttons-cotainer {
          display: flex;
          flex-direction: column;
          height: 100px;
          justify-content: space-evenly;
        }

        img {
          width: 120px;
        }

        div {
          margin-top: 16px;
        }

        h1 {
          color: ${colors.primary};
          font-weight: 700;
          margin-bottom: 16px;
        }

        h2 {
          color: ${colors.secondary};
          font-size: 18px;
          margin: 0;
        }
      `}</style>
    </>
  )
}
